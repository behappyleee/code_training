	// 친화도가 높은 코드일수록 가까이 배치(종속함수 .. 비슷한 성격 메서드 ..)
	
	// 일반적으로 함수 호출 종속성은 아래 방향으로 유지 다시 말해, 호출되는 함수를 호출하는 함수보다 나중에 배치
	
	// 좋은 소프트웨어 시스템은 읽시 쉬운 문서로 이루어진다. 스타일은 일관적이며 매끄러워야 한다.
	
