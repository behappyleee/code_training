	// 지역 변수는 각 함수 맨 처음에 선언
	
	// 인스턴스 변수 --> 인스턴스 변수는 클래스 맨 처음에 선언 (변수간에 세로로 거리를 두지 않음)
	
	// 종속 함수 --> 한 함수가 다른 함수를 호출 시 두 함수는 세로로 가까이 배치
	// 가능 하다면 호출하는 함수를 호출되는 함수보다 먼처 배치
	
